#!/usr/bin/env node
"use strict";

const fs = require("node:fs");
const path = require("node:path");
const vm = require("node:vm");

const root = path.resolve(__dirname, "..");
const configPath = path.join(root, "config.js");

function loadConfig() {
  const sandbox = { window: {} };
  vm.createContext(sandbox);
  vm.runInContext(fs.readFileSync(configPath, "utf8"), sandbox, { filename: configPath });
  return sandbox.window.QEF_SITE_CONFIG || {};
}

function buildApiUrl(baseUrl) {
  const separator = baseUrl.includes("?") ? "&" : "?";
  return `${baseUrl}${separator}action=site`;
}

async function fetchSitePayload(apiBaseUrl) {
  const response = await fetch(buildApiUrl(apiBaseUrl));
  const text = await response.text();

  if (!response.ok) {
    throw new Error(`Apps Script returned HTTP ${response.status}: ${text.slice(0, 160)}`);
  }

  try {
    return JSON.parse(text);
  } catch (error) {
    throw new Error(`Apps Script did not return JSON: ${text.slice(0, 160)}`);
  }
}

function assertPayload(payload) {
  if (!payload || payload.ok === false) {
    throw new Error((payload && payload.error) || "Apps Script site payload is not ok");
  }

  if (!Array.isArray(payload.pages) || !payload.pages.length) {
    throw new Error("Apps Script site payload has no QEF_Pages rows");
  }

  if (!Array.isArray(payload.metrics)) {
    throw new Error("Apps Script site payload metrics is not an array");
  }

  if (!Array.isArray(payload.photos)) {
    throw new Error("Apps Script site payload photos is not an array");
  }
}

function firstText() {
  for (const value of arguments) {
    const text = String(value || "").trim();
    if (text) return text;
  }

  return "";
}

function buildNextConfig(currentConfig, payload) {
  const settings = payload.settings || {};

  return {
    schoolNameZh: firstText(settings.school_name_zh, currentConfig.schoolNameZh),
    schoolNameEn: firstText(settings.school_name_en, currentConfig.schoolNameEn),
    siteTitle: firstText(settings.site_title, currentConfig.siteTitle),
    siteSubtitle: firstText(settings.site_subtitle, currentConfig.siteSubtitle),
    planTitle: firstText(settings.plan_title, currentConfig.planTitle),
    footerText: firstText(settings.footer_text, currentConfig.footerText),

    apiBaseUrl: currentConfig.apiBaseUrl || "",
    apiMode: currentConfig.apiMode || "jsonp",
    apiTimeoutMs: Number(currentConfig.apiTimeoutMs || 90000),
    useSampleDataWhenApiMissing: currentConfig.useSampleDataWhenApiMissing !== false,

    googleSheet: currentConfig.googleSheet || {},
    googleDrive: currentConfig.googleDrive || {},

    sections: payload.pages,
    metrics: payload.metrics,
    photos: payload.photos
  };
}

function serializeConfig(config) {
  return [
    "// Fallback data snapshot generated by scripts/snapshot-qef-defaults.js.",
    "// Re-run the script after Google Sheet changes that should update API fallback content.",
    `window.QEF_SITE_CONFIG = ${JSON.stringify(config, null, 2)};`,
    ""
  ].join("\n");
}

async function main() {
  const dryRun = process.argv.includes("--dry-run");
  const currentConfig = loadConfig();
  const apiBaseUrl = currentConfig.apiBaseUrl;

  if (!apiBaseUrl) {
    throw new Error("config.js apiBaseUrl is empty; cannot snapshot live QEF defaults");
  }

  const payload = await fetchSitePayload(apiBaseUrl);
  assertPayload(payload);

  const nextConfig = buildNextConfig(currentConfig, payload);
  const nextSource = serializeConfig(nextConfig);

  if (dryRun) {
    process.stdout.write(nextSource);
  } else {
    fs.writeFileSync(configPath, nextSource, "utf8");
    console.log(
      [
        "Updated config.js fallback data from live Apps Script payload.",
        `sections=${nextConfig.sections.length}`,
        `photos=${nextConfig.photos.length}`,
        `metrics=${nextConfig.metrics.length}`
      ].join(" ")
    );
  }
}

main().catch((error) => {
  console.error(`FAIL ${error && error.message ? error.message : String(error)}`);
  process.exitCode = 1;
});
